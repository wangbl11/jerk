    @Test
    @Transactional
    public void createTag() throws Exception {
        int databaseSizeBeforeCreate = tagRepository.findAll().size();

        // Create the Tag
        restTagMockMvc.perform(post("/api/tags")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(tag)))
            .andExpect(status().isCreated());

        // Validate the Tag in the database
        List<Tag> tagList = tagRepository.findAll();
        assertThat(tagList).hasSize(databaseSizeBeforeCreate + 1);
        Tag testTag = tagList.get(tagList.size() - 1);
        assertThat(testTag.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testTag.getType()).isEqualTo(DEFAULT_TYPE);
        assertThat(testTag.getStatus()).isEqualTo(DEFAULT_STATUS);
        assertThat(testTag.getWeight()).isEqualTo(DEFAULT_WEIGHT);
        assertThat(testTag.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
        assertThat(testTag.getModifiedDate()).isEqualTo(DEFAULT_MODIFIED_DATE);

        // Validate the Tag in Elasticsearch
        verify(mockTagSearchRepository, times(1)).save(testTag);
    }

